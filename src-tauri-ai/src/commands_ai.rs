/// Ask the LLM to generate a SQL expression, then add the derived column.
#[tauri::command]
pub async fn nl_transform(
    state: State<'_, AiAppState>,
    file_id: String,
    query: String,
) -> Result<TransformResult, CommandError> {
    let schema = get_schema(&state, &file_id)?;
    let llm = get_llm(&state)?;

    let (system, user_msg) = prompts::column_transform_prompt(&schema, &query);
    let resp: TransformLlmResponse = llm
        .complete_json(&system, &user_msg, 256)
        .await
        .map_err(llm_error_to_command_error)?;

    let rows_updated = {
        let stores = state.stores.lock();
        let store = stores
            .get(&file_id)
            .ok_or_else(|| CommandError::UnknownFile(file_id.clone()))?;
        store
            .add_column(&resp.column_name, &resp.expression)
            .map_err(CommandError::from)?;
        store.row_count()
    };

    Ok(TransformResult {
        expression: resp.expression,
        column_name: resp.column_name,
        rows_updated,
    })
}

