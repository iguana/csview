/// Detect statistical outliers in numeric columns, then ask the LLM to narrate.
#[tauri::command]
pub async fn detect_anomalies_cmd(
    state: State<'_, AiAppState>,
    file_id: String,
    columns: Vec<String>,
) -> Result<AnomalyReport, CommandError> {
    let schema = get_schema(&state, &file_id)?;
    let llm = get_llm(&state)?;
    let (col_metas, rows) = read_all_rows(&state, &file_id)?;

    // Determine which column indices to scan.
    let col_indices: Vec<usize> = if columns.is_empty() {
        col_metas
            .iter()
            .filter(|c| matches!(c.kind, ColumnKind::Integer | ColumnKind::Float))
            .map(|c| c.index)
            .collect()
    } else {
        columns
            .iter()
            .filter_map(|name| col_metas.iter().find(|c| &c.name == name))
            .map(|c| c.index)
            .collect()
    };

    let anomalies = csview_engine::detect_anomalies(&rows, &col_metas, &col_indices, 3.0);

    let anomaly_json = serde_json::to_string_pretty(&anomalies).unwrap_or_default();
    let (system, user_msg) = prompts::anomaly_prompt(&schema, &anomaly_json);
    let markdown = llm
        .complete(&system, &user_msg, 768)
        .await
        .map_err(llm_error_to_command_error)?;

    Ok(AnomalyReport { markdown, anomalies })
}

