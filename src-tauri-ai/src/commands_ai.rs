/// Scan for PII columns using the quality module and ask the LLM to narrate.
#[tauri::command]
pub async fn compliance_scan(
    state: State<'_, AiAppState>,
    file_id: String,
) -> Result<ComplianceReport, CommandError> {
    let schema = get_schema(&state, &file_id)?;
    let llm = get_llm(&state)?;
    let (col_metas, rows) = read_all_rows(&state, &file_id)?;

    let mut pii_by_col: HashMap<String, (String, Vec<String>)> = HashMap::new();
    let mut all_pii_issues: Vec<QualityIssueSer> = Vec::new();

    for meta in &col_metas {
        let col_vals: Vec<&str> = rows
            .iter()
            .map(|r| r.get(meta.index).map(String::as_str).unwrap_or(""))
            .collect();
        for issue in quality::audit_column(&col_vals, meta, meta.index) {
            if let IssueType::PossiblePii(ref pii) = issue.issue_type {
                let pii_str = format!("{pii:?}");
                let entry = pii_by_col
                    .entry(meta.name.clone())
                    .or_insert_with(|| (pii_str, Vec::new()));
                if entry.1.len() < 5 {
                    entry.1.push(issue.value.clone());
                }
                all_pii_issues.push(QualityIssueSer::from(&issue));
            }
        }
    }

    let pii_columns: Vec<PiiColumn> = pii_by_col
        .into_iter()
        .map(|(col_name, (pii_kind, sample_values))| PiiColumn {
            column_name: col_name,
            pii_kind,
            sample_values,
        })
        .collect();

    let pii_json = serde_json::to_string_pretty(&all_pii_issues).unwrap_or_default();
    let (system, user_msg) = prompts::compliance_prompt(&schema, &pii_json);
    let markdown = llm
        .complete(&system, &user_msg, 768)
        .await
        .map_err(llm_error_to_command_error)?;

    Ok(ComplianceReport { markdown, pii_columns })
}

