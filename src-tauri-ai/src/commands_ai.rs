/// Translate a natural-language query into a SQL WHERE clause and execute it.
#[tauri::command]
pub async fn nl_query(
    state: State<'_, AiAppState>,
    file_id: String,
    query: String,
) -> Result<NlQueryResult, CommandError> {
    // 1. Extract what we need — drop the lock before await.
    let schema = get_schema(&state, &file_id)?;
    let llm = get_llm(&state)?;
    let table = schema.table_name.clone();

    // 2. Call LLM (no locks held).
    let (system, user_msg) = prompts::nl_query_prompt(&schema, &query);
    let where_clause = llm
        .complete(&system, &user_msg, 256)
        .await
        .map_err(llm_error_to_command_error)?;
    let where_clause = where_clause.trim().to_string();

    // 3. Execute SQL — re-lock.
    let sql = format!("SELECT * FROM {table} WHERE {where_clause}");
    let result = {
        let stores = state.stores.lock();
        let store = stores
            .get(&file_id)
            .ok_or_else(|| CommandError::UnknownFile(file_id.clone()))?;
        store.query(&sql).map_err(CommandError::from)?
    };

    // 4. Persist query history.
    {
        let db = state.app_db.lock();
        let _ = db.execute(
            "INSERT INTO query_history (file_path, nl_query, generated_sql) VALUES (?1, ?2, ?3)",
            rusqlite::params![file_id, query, sql.clone()],
        );
    }

    Ok(NlQueryResult {
        where_clause,
        sql,
        columns: result.columns,
        rows: result.rows,
        row_count: result.row_count,
    })
}

