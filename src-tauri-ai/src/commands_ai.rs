/// Run the engine's quality scan and ask the LLM to narrate the findings.
#[tauri::command]
pub async fn audit_quality(
    state: State<'_, AiAppState>,
    file_id: String,
) -> Result<QualityReport, CommandError> {
    let schema = get_schema(&state, &file_id)?;
    let llm = get_llm(&state)?;
    let (col_metas, rows) = read_all_rows(&state, &file_id)?;

    let mut all_issues: Vec<QualityIssue> = Vec::new();
    for meta in &col_metas {
        let col_vals: Vec<&str> = rows
            .iter()
            .map(|r| r.get(meta.index).map(String::as_str).unwrap_or(""))
            .collect();
        let col_issues = quality::audit_column(&col_vals, meta, meta.index);
        all_issues.extend(col_issues);
    }

    let serialisable: Vec<QualityIssueSer> = all_issues.iter().map(QualityIssueSer::from).collect();
    let issues_json = serde_json::to_string_pretty(&serialisable).unwrap_or_default();

    let (system, user_msg) = prompts::quality_audit_prompt(&schema, &issues_json);
    let markdown = llm
        .complete(&system, &user_msg, 1024)
        .await
        .map_err(llm_error_to_command_error)?;

    let issue_count = serialisable.len();
    Ok(QualityReport { markdown, issues: serialisable, issue_count })
}

