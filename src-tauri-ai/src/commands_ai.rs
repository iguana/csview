/// Compute per-column statistics and ask the LLM to write a narrative report.
#[tauri::command]
pub async fn generate_profile(
    state: State<'_, AiAppState>,
    file_id: String,
) -> Result<ProfileReport, CommandError> {
    let schema = get_schema(&state, &file_id)?;
    let llm = get_llm(&state)?;
    let (columns, rows) = read_all_rows(&state, &file_id)?;

    // Compute extended stats per numeric column.
    let stats_map: HashMap<String, ExtendedColumnStats> = columns
        .iter()
        .filter(|c| matches!(c.kind, ColumnKind::Integer | ColumnKind::Float))
        .map(|c| {
            let col_vals: Vec<&str> = rows
                .iter()
                .map(|r| r.get(c.index).map(String::as_str).unwrap_or(""))
                .collect();
            let stats = stats_extended::extended_stats(&col_vals);
            (c.name.clone(), stats)
        })
        .collect();

    let stats_json = serde_json::to_string_pretty(&stats_map).unwrap_or_default();
    let (system, user_msg) = prompts::data_profile_prompt(&schema, &stats_json);
    let markdown = llm
        .complete(&system, &user_msg, 1024)
        .await
        .map_err(llm_error_to_command_error)?;

    Ok(ProfileReport {
        markdown,
        stats: serde_json::from_str(&stats_json).unwrap_or(serde_json::Value::Null),
    })
}

