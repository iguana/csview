/// Execute a join between two open `SqliteStore` instances.
///
/// Returns a `FileInfo` for the result (registered as a new open file).
#[tauri::command]
pub fn execute_join(
    state: State<'_, AiAppState>,
    left_id: String,
    right_id: String,
    left_key: String,
    right_key: String,
    join_type: String,
) -> Result<crate::commands_csv::FileInfo, CommandError> {
    use csview_engine::join::{JoinSpec, JoinType};

    let join_type_enum = match join_type.to_uppercase().as_str() {
        "LEFT" => JoinType::Left,
        "RIGHT" => JoinType::Right,
        "FULL" => JoinType::Full,
        _ => JoinType::Inner,
    };

    // Read both sides while holding the lock.
    let (left_cols, left_headers, left_rows, right_cols, right_headers, right_rows) = {
        let stores = state.stores.lock();
        let left = stores
            .get(&left_id)
            .ok_or_else(|| CommandError::UnknownFile(left_id.clone()))?;
        let right = stores
            .get(&right_id)
            .ok_or_else(|| CommandError::UnknownFile(right_id.clone()))?;

        let left_cols: Vec<ColumnMeta> = left.columns().to_vec();
        let right_cols: Vec<ColumnMeta> = right.columns().to_vec();
        let left_headers: Vec<String> = left_cols.iter().map(|c| c.name.clone()).collect();
        let right_headers: Vec<String> = right_cols.iter().map(|c| c.name.clone()).collect();

        let lq = left.query("SELECT * FROM data").map_err(CommandError::from)?;
        let rq = right.query("SELECT * FROM data").map_err(CommandError::from)?;

        let to_rows = |res: csview_engine::sqlite_store::QueryResult| -> Vec<Vec<String>> {
            res.rows
                .into_iter()
                .map(|row| {
                    row.into_iter()
                        .map(|v| match v {
                            serde_json::Value::String(s) => s,
                            serde_json::Value::Null => String::new(),
                            other => other.to_string(),
                        })
                        .collect()
                })
                .collect()
        };

        (left_cols, left_headers, to_rows(lq), right_cols, right_headers, to_rows(rq))
    };

    // Find key column indices.
    let left_key_idx = left_cols
        .iter()
        .find(|c| c.name == left_key)
        .map(|c| c.index)
        .ok_or_else(|| CommandError::InvalidArg(format!("left key column '{left_key}' not found")))?;
    let right_key_idx = right_cols
        .iter()
        .find(|c| c.name == right_key)
        .map(|c| c.index)
        .ok_or_else(|| CommandError::InvalidArg(format!("right key column '{right_key}' not found")))?;

    let spec = JoinSpec {
        left_key: left_key_idx,
        right_key: right_key_idx,
        join_type: join_type_enum,
        fuzzy: false,
    };

    let join_result =
        csview_engine::join::join_datasets(&left_headers, &left_rows, &right_headers, &right_rows, &spec)
            .map_err(|e| CommandError::Sqlite(e.to_string()))?;

    let merged_headers = join_result.headers.clone();

    // Write to a temp CSV and re-import as a new store.
    let tmp = write_rows_to_tempfile(&merged_headers, &join_result.rows)?;
    let tmp_path = tmp.path().to_string_lossy().to_string();

    let sample: Vec<Vec<String>> = join_result.rows.iter().take(200).cloned().collect();
    let inferred_cols = crate::commands_csv::infer_columns_pub(&merged_headers, &sample);

    let new_store = csview_engine::sqlite_store::SqliteStore::from_csv(
        &tmp_path,
        b',',
        true,
        &merged_headers,
        &inferred_cols,
    )
    .map_err(CommandError::from)?;

    let schema = new_store.schema_context(5).map_err(CommandError::from)?;
    let result_id = Uuid::new_v4().to_string();
    let info = crate::commands_csv::FileInfo {
        file_id: result_id.clone(),
        path: tmp_path,
        row_count: new_store.row_count(),
        columns: schema.columns,
        table_name: new_store.table_name().to_string(),
    };

    state.stores.lock().insert(result_id, new_store);
    // Leak the temp file so the path stays valid for the store's lifetime.
    std::mem::forget(tmp);

    Ok(info)
}

/// Write rows to a temporary CSV file and return the handle.
fn write_rows_to_tempfile(
    headers: &[String],
    rows: &[Vec<String>],
) -> Result<tempfile::NamedTempFile, CommandError> {
    let tmp = tempfile::NamedTempFile::new().map_err(|e| CommandError::Io(e.to_string()))?;
    let mut wtr =
        csv::Writer::from_path(tmp.path()).map_err(|e| CommandError::Csv(e.to_string()))?;
    wtr.write_record(headers).map_err(|e| CommandError::Csv(e.to_string()))?;
    for row in rows {
        wtr.write_record(row).map_err(|e| CommandError::Csv(e.to_string()))?;
    }
    wtr.flush().map_err(|e| CommandError::Io(e.to_string()))?;
    Ok(tmp)
}

