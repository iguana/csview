/// Generate and persist a structured Markdown report.
#[tauri::command]
pub async fn generate_report(
    state: State<'_, AiAppState>,
    file_id: String,
    request: String,
) -> Result<Report, CommandError> {
    let schema = get_schema(&state, &file_id)?;
    let llm = get_llm(&state)?;

    let (system, user_msg) = prompts::report_builder_prompt(&schema, &request);
    let resp: ReportLlmResponse = llm
        .complete_json(&system, &user_msg, 2048)
        .await
        .map_err(llm_error_to_command_error)?;

    let report_id = Uuid::new_v4().to_string();
    {
        let db = state.app_db.lock();
        db.execute(
            "INSERT INTO reports (id, file_path, report_type, title, content) \
             VALUES (?1, ?2, 'custom', ?3, ?4)",
            rusqlite::params![report_id, file_id, resp.title, resp.markdown],
        )
        .map_err(|e| CommandError::Sqlite(e.to_string()))?;
    }

    Ok(Report { report_id, title: resp.title, markdown: resp.markdown })
}

