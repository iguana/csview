//! AI-powered Tauri commands.
//!
//! # Locking discipline
//!
//! Every async command must:
//! 1. Lock state to clone/extract what it needs.
//! 2. **Drop the lock guard** before the first `.await`.
//! 3. Re-lock if SQLite mutations are needed after the LLM call.
//!
//! This ensures `parking_lot::Mutex` guards are never held across await points,
//! which would deadlock the Tauri async runtime.

