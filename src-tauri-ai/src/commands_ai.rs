/// Store an API key + provider + model and initialise the LLM client.
#[tauri::command]
pub async fn set_api_key(
    state: State<'_, AiAppState>,
    provider: String,
    key: String,
    model: String,
) -> Result<(), CommandError> {
    if key.trim().is_empty() {
        return Err(CommandError::InvalidArg("API key must not be empty".into()));
    }
    if model.trim().is_empty() {
        return Err(CommandError::InvalidArg(
            "Model must not be empty — pick one from the model list before saving".into(),
        ));
    }
    let prov = match provider.to_lowercase().as_str() {
        "openai" => Provider::OpenAI,
        "google" => Provider::Google,
        "anthropic" => Provider::Anthropic,
        _ => return Err(CommandError::InvalidArg(format!("unknown provider: {provider}"))),
    };
    // We deliberately do NOT validate `model` against a static allowlist —
    // providers ship new model ids regularly and we surface them via the
    // refresh button (see `fetch_provider_models`). The non-empty check
    // above is enough to prevent the "you must provide a model" 400.
    let client = LlmClient::new(prov, key.clone(), model.clone());

    {
        let db = state.app_db.lock();
        db.execute(
            "INSERT OR REPLACE INTO account (id, api_key, model) VALUES (1, ?1, ?2)",
            rusqlite::params![format!("{provider}:{key}"), model],
        )
        .map_err(|e| CommandError::Sqlite(e.to_string()))?;
    }

    *state.llm.lock() = Some(client);
    Ok(())
}

/// Check whether an API key is currently configured and return available models.
#[tauri::command]
pub fn get_account_status(state: State<'_, AiAppState>) -> Result<AccountStatus, CommandError> {
    let guard = state.llm.lock();
    let (has_api_key, provider, model) = match guard.as_ref() {
        Some(client) => (true, format!("{}", client.provider()), client.model().to_string()),
        None => (false, String::new(), String::new()),
    };
    drop(guard);
    Ok(AccountStatus {
        has_api_key,
        provider,
        model,
        available_models: available_models()
            .into_iter()
            .map(|m| AvailableModel {
                id: m.id,
                name: m.name,
                provider: format!("{}", m.provider),
                tier: format!("{:?}", m.tier).to_lowercase(),
                description: m.description,
            })
            .collect(),
    })
}

/// Fetch the live model catalogue from a provider, using a freshly-supplied
/// API key (so the user can probe before committing). Returns the same
/// `AvailableModel` shape the AccountPanel already renders.
#[tauri::command]
pub async fn fetch_provider_models(
    provider: String,
    key: String,
) -> Result<Vec<AvailableModel>, CommandError> {
    if key.trim().is_empty() {
        return Err(CommandError::InvalidArg(
            "API key required to list models".into(),
        ));
    }
    let prov = match provider.to_lowercase().as_str() {
        "openai" => Provider::OpenAI,
        "google" => Provider::Google,
        "anthropic" => Provider::Anthropic,
        _ => return Err(CommandError::InvalidArg(format!("unknown provider: {provider}"))),
    };
    // Model arg is unused for the listing endpoint; pass a placeholder.
    let client = LlmClient::new(prov, key, String::new());
    let models = client.fetch_models().await.map_err(llm_error_to_command_error)?;
    Ok(models
        .into_iter()
        .map(|m| AvailableModel {
            id: m.id,
            name: m.name,
            provider: format!("{}", m.provider),
            tier: format!("{:?}", m.tier).to_lowercase(),
            description: m.description,
        })
        .collect())
}

/// Default model for each provider when only an env-var key is found and no
/// previous selection has been persisted.
fn default_model_for(provider: Provider) -> &'static str {
    match provider {
        Provider::Anthropic => "claude-sonnet-4-20250514",
        Provider::OpenAI => "gpt-4.1",
        Provider::Google => "gemini-2.5-flash",
    }
}

/// Restore a previously saved (provider, key, model) row from the app DB.
/// Returns the configured client, or None if nothing is saved.
fn load_saved_account(state: &AiAppState) -> Option<LlmClient> {
    let db = state.app_db.lock();
    let row: Option<(String, String)> = db
        .query_row(
            "SELECT api_key, model FROM account WHERE id = 1",
            [],
            |r| Ok((r.get::<_, String>(0)?, r.get::<_, String>(1)?)),
        )
        .ok();
    drop(db);
    let (combined, model) = row?;
    // The api_key column stores "{provider}:{actual_key}" (see set_api_key).
    let (provider_str, key) = combined.split_once(':')?;
    let provider = match provider_str.to_lowercase().as_str() {
        "openai" => Provider::OpenAI,
        "google" => Provider::Google,
        "anthropic" => Provider::Anthropic,
        _ => return None,
    };
    if key.is_empty() || model.is_empty() {
        return None;
    }
    Some(LlmClient::new(provider, key.to_string(), model))
}

/// Configure the LLM client on startup. Priority:
///   1. Previously saved account in the local DB (whatever the user last picked).
///   2. ANTHROPIC_API_KEY env var → default Anthropic model.
///   3. OPENAI_API_KEY env var → default OpenAI model.
///   4. GEMINI_API_KEY env var → default Google model.
///
/// The first match wins so a user-configured choice is never silently
/// overwritten by an env var hanging around in the shell.
pub fn auto_detect_keys(state: &AiAppState) {
    if let Some(client) = load_saved_account(state) {
        *state.llm.lock() = Some(client);
        return;
    }
    let candidates = [
        ("ANTHROPIC_API_KEY", Provider::Anthropic),
        ("OPENAI_API_KEY", Provider::OpenAI),
        ("GEMINI_API_KEY", Provider::Google),
    ];
    for (env_var, provider) in candidates {
        if let Ok(key) = std::env::var(env_var) {
            if !key.is_empty() {
                let client = LlmClient::new(
                    provider,
                    key,
                    default_model_for(provider).to_string(),
                );
                *state.llm.lock() = Some(client);
                return;
            }
        }
    }
}

