fn llm_error_to_command_error(e: LlmError) -> CommandError {
    CommandError::Sqlite(format!("llm error: {e}"))
}

/// Clone the `LlmClient` out of state (or return an error if not set).
fn get_llm(state: &AiAppState) -> Result<LlmClient, CommandError> {
    state
        .llm
        .lock()
        .clone()
        .ok_or_else(|| CommandError::InvalidArg("no API key set — call set_api_key first".into()))
}

/// Extract schema context for a file while holding the lock, then release.
fn get_schema(state: &AiAppState, file_id: &str) -> Result<SchemaContext, CommandError> {
    let stores = state.stores.lock();
    let store = stores
        .get(file_id)
        .ok_or_else(|| CommandError::UnknownFile(file_id.to_string()))?;
    store.schema_context(5).map_err(CommandError::from)
}

/// Read all rows from the store as `Vec<Vec<String>>` (for engine functions).
fn read_all_rows(state: &AiAppState, file_id: &str) -> Result<(Vec<ColumnMeta>, Vec<Vec<String>>), CommandError> {
    let stores = state.stores.lock();
    let store = stores
        .get(file_id)
        .ok_or_else(|| CommandError::UnknownFile(file_id.to_string()))?;

    let cols: Vec<ColumnMeta> = store.columns().to_vec();
    let result = store.query("SELECT * FROM data").map_err(CommandError::from)?;
    let rows: Vec<Vec<String>> = result
        .rows
        .into_iter()
        .map(|row| {
            row.into_iter()
                .map(|v| match v {
                    serde_json::Value::String(s) => s,
                    serde_json::Value::Null => String::new(),
                    other => other.to_string(),
                })
                .collect()
        })
        .collect();
    Ok((cols, rows))
}

