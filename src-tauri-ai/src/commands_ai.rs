/// Fit a linear regression on `(x_col, y_col)` and ask the LLM to interpret.
#[tauri::command]
pub async fn forecast(
    state: State<'_, AiAppState>,
    file_id: String,
    x_col: String,
    y_col: String,
) -> Result<ForecastReport, CommandError> {
    let schema = get_schema(&state, &file_id)?;
    let llm = get_llm(&state)?;
    let (col_metas, rows) = read_all_rows(&state, &file_id)?;

    let x_idx = col_metas
        .iter()
        .find(|c| c.name == x_col)
        .map(|c| c.index)
        .ok_or_else(|| CommandError::InvalidArg(format!("column '{x_col}' not found")))?;
    let y_idx = col_metas
        .iter()
        .find(|c| c.name == y_col)
        .map(|c| c.index)
        .ok_or_else(|| CommandError::InvalidArg(format!("column '{y_col}' not found")))?;

    let mut xs: Vec<f64> = Vec::new();
    let mut ys: Vec<f64> = Vec::new();
    for row in &rows {
        let xv = row.get(x_idx).and_then(|s| s.trim().parse::<f64>().ok());
        let yv = row.get(y_idx).and_then(|s| s.trim().parse::<f64>().ok());
        if let (Some(x), Some(y)) = (xv, yv) {
            xs.push(x);
            ys.push(y);
        }
    }

    let regression = stats_extended::linear_regression(&xs, &ys);
    let reg_json = serde_json::to_string_pretty(&regression).unwrap_or_default();

    let (system, user_msg) = prompts::forecast_prompt(&schema, &x_col, &y_col, &reg_json);
    let markdown = llm
        .complete(&system, &user_msg, 512)
        .await
        .map_err(llm_error_to_command_error)?;

    Ok(ForecastReport {
        markdown,
        slope: regression.slope,
        intercept: regression.intercept,
        r_squared: regression.r_squared,
        n: regression.n,
        x_col,
        y_col,
    })
}
